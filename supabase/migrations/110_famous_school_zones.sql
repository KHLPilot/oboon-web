-- 110_famous_school_zones.sql
-- 전국 유명 학군 테이블 생성 및 시드 데이터
-- match_keys 포맷: "region_2depth|region_3depth" (Kakao 역지오코딩 결과 기준)

CREATE TABLE public.famous_school_zones (
  id          serial PRIMARY KEY,
  name        text NOT NULL UNIQUE,
  short_label text NOT NULL,
  tier        int  NOT NULL CHECK (tier IN (1, 2, 3)),
  city        text NOT NULL,
  description text,
  match_keys  text[] NOT NULL DEFAULT '{}',
  created_at  timestamptz NOT NULL DEFAULT now()
);

-- GIN 인덱스: match_keys 배열 검색 최적화
CREATE INDEX ON public.famous_school_zones USING GIN (match_keys);

-- RLS 활성화
ALTER TABLE public.famous_school_zones ENABLE ROW LEVEL SECURITY;

-- SELECT: 공개 허용 (anonymous + authenticated)
CREATE POLICY "famous_school_zones_select_public"
  ON public.famous_school_zones
  FOR SELECT
  USING (true);

-- INSERT/UPDATE/DELETE: service_role만 허용
CREATE POLICY "famous_school_zones_write_service_role"
  ON public.famous_school_zones
  FOR ALL
  TO service_role
  USING (true)
  WITH CHECK (true);

